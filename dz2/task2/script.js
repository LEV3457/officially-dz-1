document.addEventListener('DOMContentLoaded', function() {
    let num = prompt("Введите число ");
    if (0 == num) {
        alert(")");
    } else if (1 == num) {
        alert("!");
    } else if (2 == num) {
        alert("@");
    } else if (3 == num) {
        alert("#");
    } else if (4 == num) {
        alert("$");
    } else if (5 == num) {
        alert("%");
    } else if (6 == num) {
        alert("^");
    } else if (7 == num) {
        alert("&");
    } else if (8 == num) {
        alert("*");
    } else if (9 == num) {
        alert("(");
    } else {
        alert("не понэл")
    }


});